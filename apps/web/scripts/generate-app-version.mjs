/**
 * Generates apps/web/src/generated/appVersion.ts with a human-readable build
 * version (MAJOR.MINOR.COMMIT_COUNT), e.g. "3.1.647".
 *
 * MAJOR.MINOR is bumped by hand below for real milestones; the trailing
 * number always increments automatically (git commit count), so nobody has
 * to remember to bump a version file on every change.
 *
 * Runs automatically before `next build` via the `prebuild` npm script.
 */
import { execSync } from 'child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const MAJOR_MINOR = '3.1';
const appRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = resolve(appRoot, 'src/generated');
const outFile = resolve(outDir, 'appVersion.ts');

function resolveCommitCount() {
  try {
    const count = execSync('git rev-list --count HEAD', { cwd: appRoot, encoding: 'utf8' }).trim();
    const parsed = Number.parseInt(count, 10);
    if (Number.isFinite(parsed) && parsed > 0) {
      return parsed;
    }
  } catch {
    // git unavailable in this environment (e.g. some CI sandboxes) — fall through.
  }

  // Fallback: keep whatever was last generated so the number never goes
  // backwards, or a safe floor if this is the very first build ever.
  try {
    const existing = readFileSync(outFile, 'utf8');
    const match = existing.match(/(\d+)'\s*;?\s*$/m) ?? existing.match(/\.(\d+)'/);
    if (match) {
      return Number.parseInt(match[1], 10) + 1;
    }
  } catch {
    // no previous file either.
  }

  return 1;
}

const commitCount = resolveCommitCount();
const version = `${MAJOR_MINOR}.${commitCount}`;

if (!existsSync(outDir)) {
  mkdirSync(outDir, { recursive: true });
}

writeFileSync(
  outFile,
  `// Auto-generated by scripts/generate-app-version.mjs — do not edit by hand.\nexport const APP_VERSION = '${version}';\n`,
  'utf8'
);

// Bumping the service worker's cache name on every build makes the `activate`
// handler (which deletes any cache key other than the current one) purge
// stale precached HTML automatically on each deploy.
const swFile = resolve(appRoot, 'public/sw.js');
if (existsSync(swFile)) {
  const sw = readFileSync(swFile, 'utf8');
  const updatedSw = sw.replace(/const CACHE = '[^']*';/, `const CACHE = 'sanova-rwa-v${version}';`);
  if (updatedSw !== sw) {
    writeFileSync(swFile, updatedSw, 'utf8');
  }
}

console.log(`[generate-app-version] APP_VERSION = ${version}`);
